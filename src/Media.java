public class Media {
    public static void main (String[] args) {
        System.out.println("Cálculo da média");
        double nota1 = 5;
        double nota2 = 7.5;
        double nota3 = 8.3;
        double nota4 = 9.5;

        double media = (nota1 + nota2 + nota3 + nota4) / 4;

        System.out.println(("Sua média é:" + media));
    }
}
